-- Create Predictions Table
CREATE TABLE IF NOT EXISTS public.predictions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    device_id BIGINT,
    forecast_time TIMESTAMPTZ,
    temperature FLOAT,
    soil_moisture FLOAT,
    nitrogen FLOAT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

